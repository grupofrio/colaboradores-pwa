// ─── KOLD OS · M2 — Catálogo canónico de reglas de planeación (v1) ───────────
// Cada regla declara QUÉ incumplimiento evidencia, sobre qué entidad, con qué
// numerador/denominador del contrato del auditor, umbral, evidencia, área
// responsable y acción operativa recomendada. NINGUNA regla corrige nada:
// auto_fix = false SIEMPRE (M2 observa, no corrige).
//
// Tipos de evaluación (threshold.kind):
//   · zero      → 0 = GREEN; >0 = severidad de la regla (high→RED, medium→AMBER)
//   · min_pct   → pct >= green_at = GREEN; >= amber_at = AMBER; menor = RED
//   · max_pct   → pct <= green_at = GREEN; <= amber_at = AMBER; mayor = RED
//   · manual    → v1 sin datos en el contrato ⇒ NOT_EVALUABLE (gris, honesto)
//
// Umbrales 90/70 de cobertura: heredados del semáforo honesto de Route
// Intelligence (decisión D-A). El resto son defaults v1 PENDIENTES de
// ratificación (ver docs/m2/M2_RULE_CATALOG.md §umbrales).

