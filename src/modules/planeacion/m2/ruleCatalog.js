export function getRuleByCode(code) {
  return M2_RULES.find((r) => r.code === code) || null
}

export function rulesByCategory(categoryKey) {
  return M2_RULES.filter((r) => r.category === categoryKey)
}

export function categoryLabel(categoryKey) {
  const cat = M2_CATEGORIES.find((c) => c.key === categoryKey)
  return cat ? cat.label : categoryKey
}
